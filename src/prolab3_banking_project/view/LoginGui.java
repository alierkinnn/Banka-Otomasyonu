    private void musteriGirisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_musteriGirisActionPerformed
        ResponseModel model = loginService.login(Long.parseLong(musteriTc.getText()), musteriSifre.getText());//ÖRNEK
        
        if(model.isSuccess()==true){
            this.setVisible(false);
            new MusteriAnaMenü().setVisible(true);
            CustomerSession.getParentFrame().add(this);
        }
        else{
            JOptionPane.showMessageDialog(null, model.getMessage());
        }      
    }//GEN-LAST:event_musteriGirisActionPerformed

